                            dengan adanya LD:
                            1. Meningkatkan skill dan kemampuan belajar.
                            Layanan LD akan mendukung mahasiswa
                            dalam meningkatkan kemampuan belajar
                            antara lain: menulis essay, presentasi,
                            persiapan ujian, bahasa Inggris.

