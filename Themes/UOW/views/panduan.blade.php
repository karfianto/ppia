                            waiting period’, sehingga semua biaya-biaya
                            selama masa kehamilan hingga melahirkan bisa
                            di-claim ke pihak asuransi. Jika syarat tersebut
                            sudah kita penuhi, saya akan lanjut bercerita
                            tentang masa kehamilan istri saya di Australia.
                            Tentang kehamilan, hal pertama yang harus kita
                            lakukan adalah menemui General Practitioner
                            (GP) untuk memastikan apakah Anda atau istri
                            Anda benar-benar hamil. Pemeriksaannya adalah
                            dengan test darah (blood test). Jika memang
                            dinyatakan positif hamil, maka GP akan
                            langsung memperkirakan kapan bayi anda akan
                            lahir dan GP juga akan memberikan kartu
                            kuning (yellow card). Yellow card ini
                            digunakan sebagai kartu kontrol selama masa
                            kehamilan.
