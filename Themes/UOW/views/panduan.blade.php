                            bertugas di antenatal unit. Pada trimester kedua
                            usia kehamilan, kita hanya bisa bertemu dengan
                            midwife satu bulan sekali. Jika usia kandungan
                            sudah melewati trimester ketiga, pertemuan
                            dengan midwife bisa sampai 2 kali dalam
                            sebulan dan jika sudah mendekati hari perkiraan
                            lahir (HPL), ibu hamil (expectant mother) harus
                            bertemu midwife setiap minggu.
                            Karena ini adalah pengalaman pertama saya
                            mendampingi istri akan melahirkan anak
                            pertama, hari-hari menjelang persalinan atau
                            delivery adalah hari-hari yang paling
                            mendebarkan. Ditambah lagi tidak ada satupun
                            keluarga yang membantu menemani.
