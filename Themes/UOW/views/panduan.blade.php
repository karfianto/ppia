                            dalam bidang akademik?
                            • Manajemen waktu, buat timetable pribadi
                            dalam satu semester dan alokasikan waktu
                            untuk konsultasi dengan LD. 3-4 hari
                            sebelum due date sehingga masih ada waktu
                            untuk melakukan revisi jika ditemukan error
                            yang cukup signifikan. Misalnya draft essay
                            perlu direvisi karena tidak menjawab topic
                            question.
                            • Buat janji sesuai kebutuhan mahasiswa.
                            Lebih awal lebih baik.
                            • Pertemuan langsung akan lebih efektif
                            daripada via email, tetapi jika harus lewat
                            email atur waktu juga lebih awal.

