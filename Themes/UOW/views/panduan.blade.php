                            kita ingin menanyakan atau berkonsultasi
                            apakah ibu hamil sudah bisa di bawa ke rumah
                            sakit atau belum. Jika ibu hamil sudah benarbenar merasakan sakit atau air ketuban sudah
                            pecah, segera telepon nomor darurat tersebut dan
                            jika dibolehkan untuk dibawa ke rumah sakit,
                            segera bawa ibu hamil ke rumah sakit. Di rumah
                            sakit, ibu hamil akan ditempatkan dan
                            dibaringkan di birthing unit dan akan dicek
                            secara berkala oleh midwife sampai prosess
                            melahirkan.
