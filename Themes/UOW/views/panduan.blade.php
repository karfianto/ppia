                            satu hari yang sama. Dengan reaksi yang
                            lumayan terkejut, saya cukup kaget ketika
                            menyadari satu minggu kemudian bahwa saya
                            sudah mempunyai rencana yang mudah untuk
                            semester itu. Pada akhirnya saya berhasil
                            mendapatkan nilai yang cukup bagus untuk
                            keempat pelajaran yang saya ambil, walaupun
                            beberapa minggu kemudian saya secara terpaksa
                            harus mengambil leave selama setahun dari
                            kewajiban saya sebagai pelajar untuk rekuperasi
                            di Indonesia, dimana saya juga berhasil
                            mendapatkan kesempatan magang di suatu
                            perusahaan korporat multinasional dan
                            mendapatkan pengalaman bekerja setelah saya
                            menyelesaikan program internship tersebut.
                            Student Central juga membantu saya secara
                            mudah dalam meregistrasi ulang – sebuah
                            prosedur yang saya perlu lakukan karena saya
                            telah mengambil lebih dari enam bulan absen
                            sebagai murid internasional. Saya sejujurnya
                            cukup ragu apakah mereka akan menerima saya
                            kembali dikarenakan nilai saya yang bisa
                            terbilang menurun sejak akhir semester dua,
                            tetapi kontak balik dengan salah satu customer
                            service dari pelayanan ini, yang hanya
                            mengambil waktu kurang lebih dua minggu,
                            itupun juga karena waktu yang cukup lama
                            dihabiskan pada proses permintaan visa ke
                            kedutaan saja.<br/>
                            Sejauh ini, Student Central tidak pernah
                            memberikan saya beban dalam kelangsungan
                            saya belajar di universitas ini. Mulai dari
                            akomodasi sampai fees planning, Student
                            Central telah memberikan bantuan yang cukup
                            jelas dan selalu bersedia untuk menjawab
                            berbagai pertanyaan saya. Pepatah ‘Malu
                            bertanya, sesat dijalan’ dapat dengan mudah
                            saya masukkan di dalam konteks ini, karena
                            mungkin saja pada saat ini kalau bukan karena
                            bantuan Student Central yang nyaman dan
                            efektif, saya bisa saja sudah berada di suatu
                            tempat yang berbeda, ‘tersesat’ dari kehidupan
                            saya sekarang sebagai seorang mahasiswa di
                            University of Wollongong.
