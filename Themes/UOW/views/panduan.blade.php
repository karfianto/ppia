                            Layanan konsultasi dapat diatur sesuai
                            waktu dan kebutuhan mahasiswa dengan
                            melakukan janji terlebih dahulu dengan
                            pembimbing/konsultan dari LD. Metode
                            yang dapat digunakan adalah pertemuan
                            langsung atau melalui email.
                            4. Tepat sasaran. Mahasiswa dapat memilih
                            pembimbing/konsultan yang sudah familiar
                            dengan materi yang ditekuni. LD bekerja
