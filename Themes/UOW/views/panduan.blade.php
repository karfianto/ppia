                            khususnya birthing unit apabila ibu hamil benarbenar sudah di nyatakan mendekati proses
                            persalinan. Pernah ada pengalaman teman
                            Indonesia yang mengantar istrinya ke rumah
                            sakit dan midwife menyatakan belum saatnya
                            melahirkan, maka ibu hamil tersebut disarankan
                            untuk kembali ke rumah sambil menunggu
                            mungkin hingga bukaan 3 cm.
