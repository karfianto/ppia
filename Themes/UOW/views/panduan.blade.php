                            di Indonesia yaitu semasa kehamilan kita akan
                            disarankan untuk melakukan USG sebanyak
                            beberapa kali. Untuk berapa kali kita harus USG
                            akan di sarankan oleh GP atau radiographer. Di
                            Australia disarankan untuk tidak melakukan
                            USG terlalu sering. Selain USG, ibu hamil juga
                            diwajibkan untuk melakukan beberapa test darah
