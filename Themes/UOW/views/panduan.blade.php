                            <p>Oleh: Nur Hasyim</p>
                            Salah satu hal yang membedakan University of
                            Wollongong (UOW) dengan universitas lainnya
                            di Australia adalah saat memasuki areal
                            kampusnya. Mahasiswa atau siapapun yang
                            berkunjung tidak disambut oleh gedung megah
                            yang kerapkali menjadi landmark sebuah
                            universitas, akan tetapi mereka akan disambut
                            oleh rimbunan pohon hijau yang membungkus
                            kampus UOW. Pohon-pohon nan rindang
                            menjadikan area kampus UOW surplus produksi
                            O2 sehingga aroma segar terasa di sudut-sudut
                            kampus. Ekosistem yang begitu pas bagi para
                            pembelajar (mahasiswa, peneliti dan ilmuwan)
                            yang mengalokasikan kerja-kerja otak dengan
                            porsi yang lebih dari kerja-kerja organ tubuh
                            lainnya, lebih-lebih kerja-kerja otak ditengarai
                            membutuhkan oksigen lebih dari bentuk kerja
                            lainnya.
                            Pohon-pohon rindang tersebut bertambah manis
                            ketika dibawahnya tertata apik bangku-bangku
                            kayu berkaki beton atau baja memanjakan
                            mereka yang melepaskan penat, mengendurkan
                            urat-urat dahi yang mengernyit atau melemaskan
                            otot leher sebagai akibat syaraf mata berkerja
                            estra keras menatap layar computer dalam
                            waktu lama atau membaca berlembar-lembar
                            halaman buku atau paper.
                            Pohon-pohon rindang yang tumbuh rata di areal
                            kampus, selain menjadi lingkungan yang
                            nyaman bagi mahasiswa juga menjadi tempat
                            yang menyenangkan bagi fauna yang habitatnya
                            dipepohonan. Sehingga jangan heran jika di
                            tengah kesibukan kuliah, membaca paper,
                            mengerjakan tugas kuliah, mahasiswa dan
                            civitas akademik lainnya akan mendengar suara
                            riuh kukabara (salah satu spesies burung) yang
                            melegenda atau disaat summer tiba, denyut nadi
                            kampus UOW akan diiringi orchestra suara
                            serangga yang menyembul dari setiap
                            pepohonan. Selain kukabara, burung magpies
                            sejenis gagak yang bersuara keras atau burung
                            kakak tua berbulu putih bersih dan beberapa
                            berjambul kuning terlihat lalu lalang dari satu
                            pohon ke pohon lain atau dari satu gedung ke
                            gedung lainnya.
                            Tak mengherankan jika anak salah seorang
                            mahasiswa Indonesia yang masih balita mengira
                            bahwa kampus ayahnya dan ibunya adalah
                            kebun binatang, kampus UOW mungkin
                            mengingatkannya akan suara-suara burung di
                            Gembiraloka atau Ragunan atau di kebun-kebun
                            binatang lainnya di Indonesia.
                            Lokasi kampus yang tepat berseberangan dengan
                            Wollongong Botanic Garden membuat kampus
                            UOW tak ubahnya kembaran taman yang setiap
                            harinya ramai dikunjungi orang dengan gratis
                            itu, sekedar untuk jalan-jalan, jogging atau
                            mengajak bermain anak.
                            Yang membedakan kampus UOW dengan
                            Botanic Garden adalah deretan mobil yang
                            terparkir rapi di taman-taman parkir kampus
                            serta lalu lalang orang menenteng buku atau
                            bahkan menyeret koper yang berisi buku. Selain
                            itu, di saat malam hari, lampu-lampu kampus
                            menyala terang serta ruang-ruang kerja
                            mahasiswa riset yang seperti tak pernah sepi
                            serta perpustakaan kampus yang tetap buka saat
                            malam bahkan untuk waktu-waktu tertentu buka
                            sampai jam 10 malam.
                            Selain deretan pohon-pohon hijau, beberapa
                            sudut kampus juga dilengkapi dengan ruang
                            terbuka dengan hamparan rumput hijau yang
                            menggoda siapa saja untuk merebahkan diri atau
                            sekedar duduk-duduk sambil makan siang atau
                            menikmati kopi atau teh panas. Hamparan
                            rumput itu biasanya ramai pada saat makan
                            siang. Pada musim dingin dan ketika matahari
                            bersinar cerah pelataran rumput ini menjadi
                            tempat favorit mahasiswa untuk beristirahat atau
                            menghangat diri dari sergapan hawa dingin yang
                            menusuk.
                            Kampus UOW yang hijau dengan beberapa area
                            berumput yang terawat menjadi sempurna
                            dengan adanya aliran sungai kecil yang
                            membelah kampus yang salah satunya mengaliri
                            beberapa kolam yang menjadi rumah
                            mengasikkan bagi spesies bebek bahkan ada satu
                            kolam yang sangat terkenal di UOW dengan
                            sebutan Duck Pond dan menjadi meeting point
                            favorit mahasiswa serta menjadi pusat kegiatan
                            terutama pada masa orientasi bagi mahasiswa
                            baru. Kehijauan kampus UOW memberikan
                            alasan yang rasional mengapa memilih kuliah di
                            UOW.
