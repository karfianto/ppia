                            <p>Oleh: Wahyu Caesarendra dan Dyah Kurniasari</p>
                            Memiliki anak yang lahir di Australia khususnya
                            di kota Wollongong adalah pengalaman
                            berharga dan anugerah istimewa yang pernah
                            saya alami. Melalui tulisan ini saya ingin berbagi
                            pengalaman pribadi tentang istri saya yang hamil
                            dan melahirkan di Wollongong. Semoga
                            bermanfaat bagi pembaca di kemudian hari.
                            Sedikit info pembuka, saya memulai studi S3 di
                            University of Wollongong (UOW) pada bulan
                            February 2011 dan anak pertama saya lahir pada
                            bulan January 2013. Saya akan memulai cerita
                            pendek ini dari persyaratan tentang ‘pregnancy
                            related services’ dan ‘giving birth’ atau
                            ‘delivery’ dari asuransi kesehatan yang kami
                            punya.
                            Sudah merupakan keharusan untuk setiap warga
                            negara asing non Australia yang studi di
                            Australia untuk memiliki asuransi kesehatan
                            yang bernama Overseas Health Cover (OSHC).
                            Di awal studi S3 sampai dengan 3 tahun saya
                            menggunakan Asuransi OSHC dari AHM.
                            Dikarenakan AHM sudah tidak memiliki
                            kerjasama dengan UOW maka di sisa durasi S3
                            saya, saya menggunakan asuransi OSHC dari
                            BUPA yang saat ini memiliki kerjasama dengan
                            UOW. Jika ditelusuri lebih lanjut di website
                            AHM dan BUPA, ada sekilas informasi yang
                            tertulis sebagai berikut: ’12 months waiting
                            period’, kalimat ini artinya adalah semua biayabiaya yang berkaitan dengan ‘pregnancy related
                            services’ seperti ultrasound (USG), blood test,
                            dan lain-lain sampai dengan proses melahirkan
                            (delivery) tidak akan ditanggung asuransi jika
                            durasi keanggotaan kita kurang dari 12 bulan.
